public class CustomAdapterReportItem {

    private String data;
    private String date;
    private String mealoption;

    public String getData() {
        return data;
    }
    public void setData(String mdata) { this.data = mdata; }

    public String getDate() {
        return date;
    }
    public void setDate(String mdate) {
        this.date = mdate;
    }

    public String getMeal() {
        return mealoption;
    }
    public void setMeal(String meal) {
        this.mealoption = meal;
    }
}
